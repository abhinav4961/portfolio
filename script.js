function toggleSkills() {
    const skillsContent = document.getElementById('skills-content');
    skillsContent.style.display = skillsContent.style.display === 'none' ? 'block' : 'none';
}

function toggleInterests() {
    const interestsContent = document.getElementById('interests-content');
    interestsContent.style.display = interestsContent.style.display === 'none' ? 'block' : 'none';
}

